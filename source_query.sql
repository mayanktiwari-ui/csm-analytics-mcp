LEFT JOIN converted_ticket_time ctt
    ON t.id = ctt.id
LEFT JOIN CSAT_Chat cst
    ON t.CUSTOM_CF_FC_INTERACTION_ID = cst.INTERACTION_RAW_ID
LEFT JOIN PROD_HIGHLEVEL_DWH.FRESHDESK.agent fa
    ON t.responder_id = fa.id
LEFT JOIN PROD_HIGHLEVEL_DWH.EFFY_FRESHCHAT.AGENTS a
    ON a.EMAIL = fa.CONTACT_EMAIL
LEFT JOIN PROD_HIGHLEVEL_DWH.FRESHDESK.COMPANY comp
    ON t.CUSTOM_CF_STRIPE_CUSTOMER_ID = comp.CUSTOM_STRIPE_CUSTOMER_ID
LEFT JOIN PROD_HIGHLEVEL_DWH.google_sheets.impl_staffing_tracker st
    ON fa.CONTACT_EMAIL = LOWER(st.EMAIL)
LEFT JOIN PROD_HIGHLEVEL_DWH.MOZART.NEW_TRIALS nt
    ON TRY_CAST(NULLIF(t.CUSTOM_CF_RELATIONSHIP_NUMBER, '') AS NUMBER) = TRY_CAST(NULLIF(nt.CUSTOMER_METADATA_RELATIONSHIP_NUMBER, '') AS NUMBER)
LEFT JOIN weekly_available_reps wr
    ON wr.week_start = DATEADD(DAY, -1, DATE_TRUNC('WEEK', CONVERT_TIMEZONE('America/Chicago', t.created_at)))
LEFT JOIN booked_csat bc
    ON t.ID = TRY_CAST(NULLIF(bc.TICKET_ID_, '') AS NUMBER)

