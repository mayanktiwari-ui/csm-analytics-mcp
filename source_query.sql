    t.created_at,
    TO_CHAR(CONVERT_TIMEZONE('America/Chicago', ctt.CREATED_AT_NEW), 'YYYY-MM-DD HH24:MI:SS') AS CREATED_AT_NEW,
    DATEADD(DAY, -1, DATE_TRUNC('WEEK', CONVERT_TIMEZONE('America/Chicago', t.created_at))) AS WEEK_CREATED_AT_NEW,
    EXTRACT(HOUR FROM CONVERT_TIMEZONE('UTC', 'America/Chicago', TO_TIMESTAMP_TZ(t.created_at)::timestamp_ntz)) AS hour_of_day,

    t.GROUP_ID,
    t.COMPANY_ID AS FD_COMPANY_ID,
    t.PRIORITY,
    t.FR_ESCALATED,
    t.SUBJECT,
    t.TYPE,
    t.DELETED,
    t.IS_ESCALATED,
    t.DESCRIPTION_TEXT,
    t.CUSTOM_CF_RELATIONSHIP_NUMBER,
    t.CUSTOM_CF_HELP_DOC_PROVIDED_LEVEL_1_HELP_DOC_DEFLECTION,
