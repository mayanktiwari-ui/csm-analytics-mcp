WITH week_dates AS (
    SELECT DISTINCT
        DATEADD(DAY, -1, DATE_TRUNC('WEEK', CONVERT_TIMEZONE('America/Chicago', t.created_at))) AS week_start
    FROM PROD_HIGHLEVEL_DWH.FRESHDESK.TICKET t
    WHERE t.GROUP_ID IN (
        155000000443, 155000000512, 155000000491, 155000000505,
        155000000488, 155000000526, 155000000506, 155000000314,
        48000365697,  155000000308, 155000000528, 155000000529,
        155000000527, 155000000500, 155000000280, 155000000485,
        155000000151
    )
      AND (t.DELETED IS NULL OR t.DELETED = FALSE)
      AND t.created_at >= '2025-04-07'
),

weekly_available_reps AS (
    SELECT
        wd.week_start,
        COUNT(DISTINCT st.email) AS available_reps
    FROM week_dates wd
    JOIN PROD_HIGHLEVEL_DWH.google_sheets.impl_staffing_tracker st
        ON st.role IN ('CSM I', 'CSM II', 'Frontline')
        AND st.available_date <= wd.week_start
        AND (
            st.end_date IS NULL
            OR DATEADD(DAY, -1, DATE_TRUNC('WEEK', CONVERT_TIMEZONE('America/Chicago', st.end_date))) >= wd.week_start
        )
    GROUP BY wd.week_start
),

CSAT_Chat AS (
    SELECT
        INTERACTION_RAW_ID,
        CASE
            WHEN COUNT(CASE WHEN LOWER(actor_type) = 'agent' THEN 1 END) > 0
             AND COUNT(CASE WHEN LOWER(actor_type) = 'user' THEN 1 END) > 0
            THEN 'Yes'
            ELSE 'No'
        END AS csat_sent_flag
    FROM PROD_HIGHLEVEL_DWH.EFFY_FRESHCHAT.CHAT_TRANSCRIPT
    GROUP BY INTERACTION_RAW_ID
),

converted_ticket_time AS (
    SELECT ID, created_at, CONVERT_TIMEZONE('America/Chicago', created_at) AS CREATED_AT_NEW
    FROM PROD_HIGHLEVEL_DWH.FRESHDESK.TICKET
),

booked_csat AS (
    SELECT
        TICKET_ID_,
        ROUND(
            (
                COALESCE(CASE
                    WHEN UPPER(Q_1_S_1_HOW_WOULD_YOU_RATE_THE_CLARITY_AND_SUPPORT_PROVIDED_BY_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%VERY CLEAR%' THEN 5
                    WHEN UPPER(Q_1_S_1_HOW_WOULD_YOU_RATE_THE_CLARITY_AND_SUPPORT_PROVIDED_BY_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%CLEAR AND GENERALLY%' THEN 4
                    WHEN UPPER(Q_1_S_1_HOW_WOULD_YOU_RATE_THE_CLARITY_AND_SUPPORT_PROVIDED_BY_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%NEUTRAL%' THEN 3
                    WHEN UPPER(Q_1_S_1_HOW_WOULD_YOU_RATE_THE_CLARITY_AND_SUPPORT_PROVIDED_BY_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%SOMEWHAT UNCLEAR%' THEN 2
                    WHEN UPPER(Q_1_S_1_HOW_WOULD_YOU_RATE_THE_CLARITY_AND_SUPPORT_PROVIDED_BY_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%VERY UNCLEAR%' THEN 1
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(Q_4_S_1_HOW_CONFIDENT_DO_YOU_FEEL_USING_HIGH_LEVEL_TO_ACHIEVE_YOUR_GOALS_) LIKE '%VERY CONFIDENT%' THEN 5
                    WHEN UPPER(Q_4_S_1_HOW_CONFIDENT_DO_YOU_FEEL_USING_HIGH_LEVEL_TO_ACHIEVE_YOUR_GOALS_) LIKE '%MODERATELY CONFIDENT%' THEN 3
                    WHEN UPPER(Q_4_S_1_HOW_CONFIDENT_DO_YOU_FEEL_USING_HIGH_LEVEL_TO_ACHIEVE_YOUR_GOALS_) LIKE '%SLIGHTLY CONFIDENT%' THEN 2
                    WHEN UPPER(Q_4_S_1_HOW_CONFIDENT_DO_YOU_FEEL_USING_HIGH_LEVEL_TO_ACHIEVE_YOUR_GOALS_) LIKE '%NOT AT ALL CONFIDENT%' THEN 1
                    WHEN UPPER(Q_4_S_1_HOW_CONFIDENT_DO_YOU_FEEL_USING_HIGH_LEVEL_TO_ACHIEVE_YOUR_GOALS_) LIKE '%CONFIDENT%' THEN 4
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(Q_1_S_2_HOW_EASY_HAS_IT_BEEN_TO_IMPLEMENT_WHAT_YOU_VE_LEARNED_SO_FAR_) LIKE '%VERY EASY%' THEN 5
                    WHEN UPPER(Q_1_S_2_HOW_EASY_HAS_IT_BEEN_TO_IMPLEMENT_WHAT_YOU_VE_LEARNED_SO_FAR_) LIKE '%GENERALLY EASY%' THEN 4
                    WHEN UPPER(Q_1_S_2_HOW_EASY_HAS_IT_BEEN_TO_IMPLEMENT_WHAT_YOU_VE_LEARNED_SO_FAR_) LIKE '%NEUTRAL%' THEN 3
                    WHEN UPPER(Q_1_S_2_HOW_EASY_HAS_IT_BEEN_TO_IMPLEMENT_WHAT_YOU_VE_LEARNED_SO_FAR_) LIKE '%SOMEWHAT DIFFICULT%' THEN 2
                    WHEN UPPER(Q_1_S_2_HOW_EASY_HAS_IT_BEEN_TO_IMPLEMENT_WHAT_YOU_VE_LEARNED_SO_FAR_) LIKE '%VERY DIFFICULT%' THEN 1
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(Q_3_S_2_HOW_SATISFIED_ARE_YOU_WITH_THE_SUPPORT_AND_GUIDANCE_FROM_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%VERY SATISFIED%' THEN 5
                    WHEN UPPER(Q_3_S_2_HOW_SATISFIED_ARE_YOU_WITH_THE_SUPPORT_AND_GUIDANCE_FROM_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%SOMEWHAT SATISFIED%' THEN 4
                    WHEN UPPER(Q_3_S_2_HOW_SATISFIED_ARE_YOU_WITH_THE_SUPPORT_AND_GUIDANCE_FROM_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%NEUTRAL%' THEN 3
                    WHEN UPPER(Q_3_S_2_HOW_SATISFIED_ARE_YOU_WITH_THE_SUPPORT_AND_GUIDANCE_FROM_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%SOMEWHAT DISSATISFIED%' THEN 2
                    WHEN UPPER(Q_3_S_2_HOW_SATISFIED_ARE_YOU_WITH_THE_SUPPORT_AND_GUIDANCE_FROM_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%VERY DISSATISFIED%' THEN 1
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(Q_1_S_3_HOW_EFFECTIVE_WAS_YOUR_CUSTOMER_SUCCESS_MANAGER_IN_ADDRESSING_YOUR_QUESTIONS_AND_CONCERNS_) LIKE '%VERY EFFECTIVE%' THEN 5
                    WHEN UPPER(Q_1_S_3_HOW_EFFECTIVE_WAS_YOUR_CUSTOMER_SUCCESS_MANAGER_IN_ADDRESSING_YOUR_QUESTIONS_AND_CONCERNS_) LIKE '%SOMEWHAT EFFECTIVE%' THEN 4
                    WHEN UPPER(Q_1_S_3_HOW_EFFECTIVE_WAS_YOUR_CUSTOMER_SUCCESS_MANAGER_IN_ADDRESSING_YOUR_QUESTIONS_AND_CONCERNS_) LIKE '%NEUTRAL%' THEN 3
                    WHEN UPPER(Q_1_S_3_HOW_EFFECTIVE_WAS_YOUR_CUSTOMER_SUCCESS_MANAGER_IN_ADDRESSING_YOUR_QUESTIONS_AND_CONCERNS_) LIKE '%SOMEWHAT INEFFECTIVE%' THEN 2
                    WHEN UPPER(Q_1_S_3_HOW_EFFECTIVE_WAS_YOUR_CUSTOMER_SUCCESS_MANAGER_IN_ADDRESSING_YOUR_QUESTIONS_AND_CONCERNS_) LIKE '%VERY INEFFECTIVE%' THEN 1
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(Q_2_S_3_HOW_WELL_IS_HIGH_LEVEL_HELPING_YOU_ACHIEVE_YOUR_INTENDED_GOALS_) LIKE '%VERY CLEAR%' THEN 5
                    WHEN UPPER(Q_2_S_3_HOW_WELL_IS_HIGH_LEVEL_HELPING_YOU_ACHIEVE_YOUR_INTENDED_GOALS_) LIKE '%CLEAR AND GENERALLY%' THEN 4
                    WHEN UPPER(Q_2_S_3_HOW_WELL_IS_HIGH_LEVEL_HELPING_YOU_ACHIEVE_YOUR_INTENDED_GOALS_) LIKE '%NEUTRAL%' THEN 3
                    WHEN UPPER(Q_2_S_3_HOW_WELL_IS_HIGH_LEVEL_HELPING_YOU_ACHIEVE_YOUR_INTENDED_GOALS_) LIKE '%SOMEWHAT UNCLEAR%' THEN 2
                    WHEN UPPER(Q_2_S_3_HOW_WELL_IS_HIGH_LEVEL_HELPING_YOU_ACHIEVE_YOUR_INTENDED_GOALS_) LIKE '%VERY UNCLEAR%' THEN 1
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(Q_1_S_4_HOW_LIKELY_ARE_YOU_TO_RECOMMEND_HIGH_LEVEL_TO_OTHERS_) LIKE '%VERY LIKELY%' THEN 5
                    WHEN UPPER(Q_1_S_4_HOW_LIKELY_ARE_YOU_TO_RECOMMEND_HIGH_LEVEL_TO_OTHERS_) LIKE '%SOMEWHAT LIKELY%' THEN 4
                    WHEN UPPER(Q_1_S_4_HOW_LIKELY_ARE_YOU_TO_RECOMMEND_HIGH_LEVEL_TO_OTHERS_) LIKE '%NEUTRAL%' THEN 3
                    WHEN UPPER(Q_1_S_4_HOW_LIKELY_ARE_YOU_TO_RECOMMEND_HIGH_LEVEL_TO_OTHERS_) LIKE '%SOMEWHAT UNLIKELY%' THEN 2
                    WHEN UPPER(Q_1_S_4_HOW_LIKELY_ARE_YOU_TO_RECOMMEND_HIGH_LEVEL_TO_OTHERS_) LIKE '%VERY UNLIKELY%' THEN 1
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(Q_2_S_4_HOW_SATISFIED_ARE_YOU_WITH_YOUR_OVERALL_EXPERIENCE_WITH_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%VERY SATISFIED%' THEN 5
                    WHEN UPPER(Q_2_S_4_HOW_SATISFIED_ARE_YOU_WITH_YOUR_OVERALL_EXPERIENCE_WITH_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%SOMEWHAT SATISFIED%' THEN 4
                    WHEN UPPER(Q_2_S_4_HOW_SATISFIED_ARE_YOU_WITH_YOUR_OVERALL_EXPERIENCE_WITH_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%NEUTRAL%' THEN 3
                    WHEN UPPER(Q_2_S_4_HOW_SATISFIED_ARE_YOU_WITH_YOUR_OVERALL_EXPERIENCE_WITH_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%SOMEWHAT DISSATISFIED%' THEN 2
                    WHEN UPPER(Q_2_S_4_HOW_SATISFIED_ARE_YOU_WITH_YOUR_OVERALL_EXPERIENCE_WITH_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE '%VERY DISSATISFIED%' THEN 1
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(PAC_Q_1_HOW_SATISFIED_ARE_YOU_WITH_THE_CALL_) LIKE '%VERY SAT%' THEN 5
                    WHEN UPPER(PAC_Q_1_HOW_SATISFIED_ARE_YOU_WITH_THE_CALL_) LIKE '%VERY DISSATISFIED%' THEN 1
                    WHEN UPPER(PAC_Q_1_HOW_SATISFIED_ARE_YOU_WITH_THE_CALL_) LIKE '%DISSATISFIED%' THEN 2
                    WHEN UPPER(PAC_Q_1_HOW_SATISFIED_ARE_YOU_WITH_THE_CALL_) LIKE '%NEUTRAL%' THEN 3
                    WHEN UPPER(PAC_Q_1_HOW_SATISFIED_ARE_YOU_WITH_THE_CALL_) LIKE '%SATISFIED%' THEN 4
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(PAC_Q_2_HOW_VALUABLE_WAS_THIS_SESSION_IN_HELPING_YOU_ADOPT_AND_USE_THE_FEATURE_) LIKE '%EXTREMELY VALUABLE%' THEN 5
                    WHEN UPPER(PAC_Q_2_HOW_VALUABLE_WAS_THIS_SESSION_IN_HELPING_YOU_ADOPT_AND_USE_THE_FEATURE_) LIKE '%VERY VALUABLE%' THEN 5
                    WHEN UPPER(PAC_Q_2_HOW_VALUABLE_WAS_THIS_SESSION_IN_HELPING_YOU_ADOPT_AND_USE_THE_FEATURE_) LIKE '%MODERATELY VALUABLE%' THEN 4
                    WHEN UPPER(PAC_Q_2_HOW_VALUABLE_WAS_THIS_SESSION_IN_HELPING_YOU_ADOPT_AND_USE_THE_FEATURE_) LIKE '%SLIGHT VALUABLE%' THEN 3
                    WHEN UPPER(PAC_Q_2_HOW_VALUABLE_WAS_THIS_SESSION_IN_HELPING_YOU_ADOPT_AND_USE_THE_FEATURE_) LIKE '%NOT VALUABLE%' THEN 1
                    WHEN UPPER(PAC_Q_2_HOW_VALUABLE_WAS_THIS_SESSION_IN_HELPING_YOU_ADOPT_AND_USE_THE_FEATURE_) LIKE '%VALUABLE%' THEN 4
                END, 0)
                + COALESCE(CASE
                    WHEN UPPER(PAC_Q_3_AFTER_THE_CALL_HOW_CONFIDENT_DO_YOU_FEEL_IN_USING_THE_PRODUCT_FEATURES_WE_COVERED_) LIKE '%VERY CONFIDENT%' THEN 5
                    WHEN UPPER(PAC_Q_3_AFTER_THE_CALL_HOW_CONFIDENT_DO_YOU_FEEL_IN_USING_THE_PRODUCT_FEATURES_WE_COVERED_) LIKE '%MODERATELY CONFIDENT%' THEN 3
                    WHEN UPPER(PAC_Q_3_AFTER_THE_CALL_HOW_CONFIDENT_DO_YOU_FEEL_IN_USING_THE_PRODUCT_FEATURES_WE_COVERED_) LIKE '%SLIGHTLY CONFIDENT%' THEN 2
                    WHEN UPPER(PAC_Q_3_AFTER_THE_CALL_HOW_CONFIDENT_DO_YOU_FEEL_IN_USING_THE_PRODUCT_FEATURES_WE_COVERED_) LIKE '%NOT AT ALL CONFIDENT%' THEN 1
                    WHEN UPPER(PAC_Q_3_AFTER_THE_CALL_HOW_CONFIDENT_DO_YOU_FEEL_IN_USING_THE_PRODUCT_FEATURES_WE_COVERED_) LIKE '%CONFIDENT%' THEN 4
                END, 0)
            )
            /
            NULLIF(
                IFF(UPPER(Q_1_S_1_HOW_WOULD_YOU_RATE_THE_CLARITY_AND_SUPPORT_PROVIDED_BY_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE ANY ('%CLEAR%','%NEUTRAL%','%UNCLEAR%'), 1, 0)
              + IFF(UPPER(Q_4_S_1_HOW_CONFIDENT_DO_YOU_FEEL_USING_HIGH_LEVEL_TO_ACHIEVE_YOUR_GOALS_) LIKE ANY ('%CONFIDENT%','%NOT AT ALL%'), 1, 0)
              + IFF(UPPER(Q_1_S_2_HOW_EASY_HAS_IT_BEEN_TO_IMPLEMENT_WHAT_YOU_VE_LEARNED_SO_FAR_) LIKE ANY ('%EASY%','%NEUTRAL%','%DIFFICULT%'), 1, 0)
              + IFF(UPPER(Q_3_S_2_HOW_SATISFIED_ARE_YOU_WITH_THE_SUPPORT_AND_GUIDANCE_FROM_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE ANY ('%SATISFIED%','%NEUTRAL%','%DISSATISFIED%'), 1, 0)
              + IFF(UPPER(Q_1_S_3_HOW_EFFECTIVE_WAS_YOUR_CUSTOMER_SUCCESS_MANAGER_IN_ADDRESSING_YOUR_QUESTIONS_AND_CONCERNS_) LIKE ANY ('%EFFECTIVE%','%NEUTRAL%','%INEFFECTIVE%'), 1, 0)
              + IFF(UPPER(Q_2_S_3_HOW_WELL_IS_HIGH_LEVEL_HELPING_YOU_ACHIEVE_YOUR_INTENDED_GOALS_) LIKE ANY ('%CLEAR%','%NEUTRAL%','%UNCLEAR%'), 1, 0)
              + IFF(UPPER(Q_1_S_4_HOW_LIKELY_ARE_YOU_TO_RECOMMEND_HIGH_LEVEL_TO_OTHERS_) LIKE ANY ('%LIKELY%','%NEUTRAL%','%UNLIKELY%'), 1, 0)
              + IFF(UPPER(Q_2_S_4_HOW_SATISFIED_ARE_YOU_WITH_YOUR_OVERALL_EXPERIENCE_WITH_YOUR_CUSTOMER_SUCCESS_MANAGER_) LIKE ANY ('%SATISFIED%','%NEUTRAL%','%DISSATISFIED%'), 1, 0)
              + IFF(UPPER(PAC_Q_1_HOW_SATISFIED_ARE_YOU_WITH_THE_CALL_) LIKE ANY ('%SAT%','%NEUTRAL%','%DISSATISFIED%'), 1, 0)
              + IFF(UPPER(PAC_Q_2_HOW_VALUABLE_WAS_THIS_SESSION_IN_HELPING_YOU_ADOPT_AND_USE_THE_FEATURE_) LIKE ANY ('%VALUABLE%'), 1, 0)
              + IFF(UPPER(PAC_Q_3_AFTER_THE_CALL_HOW_CONFIDENT_DO_YOU_FEEL_IN_USING_THE_PRODUCT_FEATURES_WE_COVERED_) LIKE ANY ('%CONFIDENT%','%NOT AT ALL%'), 1, 0)
            , 0)
        , 2) AS booked_CSAT
    FROM PROD_HIGHLEVEL_DWH.google_sheets.csm_csats_all_call_type
)

